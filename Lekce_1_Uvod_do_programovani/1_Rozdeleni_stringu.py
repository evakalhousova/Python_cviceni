# V této úloze budeš pracovat s indexy datového typu str.
# Pomocí indexů a operací slicing, striding zpracuj zadané hodnoty tak, ať dostaneš:
# Prvních 5 písmen slova 'indexování',
# posledních 5 písmen slova 'indexování',
# každé třetí písmeno slova 'indexování' (počínaje prvním písmenem "i"),
# všechny výstupy zapiš přímo do funkce print,
# výstup dále úprav podle ukázky níže.
# Ukázka průběhu:
# Prvních 5 písmen:
# index
# Posledních 5 písmen:
# ování
# Každé 3. písmeno (počínaje prvním) od 'i':
# ieví
# Poznámka: Při speficikaci v hranatých závorkách použij vždy jen jedno číslo.

print('Prvních 5 písmen:')
print('indexování'[:5])
print('Posledních 5 písmen:')
print('indexování'[-5:])
print("Každé 3. písmeno (počínaje prvním) od 'i':")
print('indexování'[::3])
